public class datatype {
    public static void main(String[] args) {
//        primitive datatype
//        1 boolean
//        2 byte
//        3 short
//        4 int
//        5 long
//        6 double
//        7 float
//        8 char
        boolean a = false;
        System.out.println(a);
        boolean b = false;
        System.out.println(b);

        byte u = 124;
        System.out.println(a);
        byte c = 126;
//        System.out.println(c);     range -128 to 127  size 8- bit

        short age = 6788;
        System.out.println(age);
        short salary = 32655;
        System.out.println(salary);
//        range -32,768 to 32,767 size 16 bits


        int fixed = 28908098;
        System.out.println(fixed);
        int kutte = 6788878;
        System.out.println(kutte);
//     range -2ki power 31 to 2 ki power 31 -1  sized 32 bit


        long hello = 73884784939l;
        System.out.println(hello);
        long bye = 873848748l;
        System.out.println(bye);
//        range -2 ki power 63 to 2 ki power 63 -1  64 bit

        double kaisw_ho = 3.897;
        System.out.println(kaisw_ho);
        double kaisw_ho2 = 3.81234567891011121314;
        System.out.println(kaisw_ho2);
//       64 bit using decimal no



        float thik_hpo = 14.99798f;
        System.out.println(thik_hpo);

        float thik_hpo2 = 14.1234567891011121314151617181920f;
        System.out.println(thik_hpo2);
//        32 bit  less precise than double f use krnA




        char e = 'A';
        System.out.println(e);
}}
